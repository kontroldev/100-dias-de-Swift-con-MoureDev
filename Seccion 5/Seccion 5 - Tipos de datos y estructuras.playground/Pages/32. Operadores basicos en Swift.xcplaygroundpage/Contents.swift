import Foundation


// Operador de asignacion '='
let numeroConstante = 18
var numeroVariable = 2

//Operadores Aritmeticos + - * /
numeroConstante + numeroVariable  // 18 + 2
numeroConstante - numeroVariable  // 18 - 2
numeroConstante * numeroVariable  // 18 + 2
numeroConstante / numeroVariable  // 18 / 2

//Operadores compuestos
numeroVariable += 2   // 2 += 2  // 'numeroVariable' tiene un valor acumulativo
numeroVariable -= 2   // 4 -= 2
numeroVariable *= 2   // 2 *= 2
numeroVariable /= 2   // 4 /= 2


// OPERADORES DE COMPARACION
var x = 1
var y = 2

x == y // igual que
x != y // es diferente o no igual
x > y  // es mayor que
x < y // es menor que
x >= y // es mayor o igual
x <= y // es menor o igual

