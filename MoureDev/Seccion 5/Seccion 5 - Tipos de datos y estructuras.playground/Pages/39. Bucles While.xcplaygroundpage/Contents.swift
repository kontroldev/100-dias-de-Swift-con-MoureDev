import Foundation

/*Los bucle while en programación es una estructura de control de flujo que permite ejecutar repetidamente un bloque de código mientras una condición específica sea verdadera.*/

var age = 10

while age < 18 {
    print("Eres menor de edad. Te faltan \(18 - age) años.")
    age += 1
}
 print("Ya eres mayor de edad.")

